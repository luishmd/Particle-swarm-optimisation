import math

#----------------------------------------------------------------------------------------
# FUNCTIONS
#----------------------------------------------------------------------------------------
def model_polynomial(inputs):
    # extract inputs values
    x1 = inputs['x1']
    x2 = inputs['x2']

    # evaluate model
    output = math.sqrt(x1) + 3*x2**2

    return output


